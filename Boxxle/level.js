export function compteur()  {
    let deuxEgale4 = false;
    let chiffreTrois = false;
    let deuxEtQuatrePresent = false;
    let bon_tableaux = 0
  
    for (let i = 0; i < Levels.length; i++) {
        deuxEgale4 = false;
        chiffreTrois = false;
        deuxEtQuatrePresent = false;
        bon_tableaux = 0
        const level = Levels[i];
        let nbDeux = 0;
        let nbQuatre = 0;
        let aChiffreTrois = false;
        let trop = false
    
        for (let j = 0; j < level.length; j++) {
            const row = level[j];
            for (let k = 0; k < row.length; k++) {
            const cell = row[k];
    
                if (cell === 2) {
                    nbDeux++;
                } else if (cell === 4) {
                    nbQuatre++;
                } else if (cell === 3) {
                    if (aChiffreTrois == true) {
                        trop = true;
                    }else{
                        aChiffreTrois = true;
                    }
                }
            }
        }
  
        if (nbDeux === nbQuatre) {
            deuxEgale4 = true;
        }
    
        if (aChiffreTrois == true) {
            if (trop == true) {
                chiffreTrois = false;
            }else{
                chiffreTrois = true;
            }
        }
    
        if (nbDeux > 0 && nbQuatre > 0) {
            deuxEtQuatrePresent = true;
        }

        if (deuxEgale4 == false || chiffreTrois == false || deuxEtQuatrePresent == false ) {
            console.log(`deuxEgale4 : ${deuxEgale4}`);
            console.log(`chiffreTrois : ${chiffreTrois}`);
            console.log(`deuxEtQuatrePresent : ${deuxEtQuatrePresent}`);
            bon_tableaux = i -1
            console.log(bon_tableaux);
            return { deuxEgale4, chiffreTrois, deuxEtQuatrePresent, bon_tableaux };
        }
    }

    console.log(`deuxEgale4 : ${deuxEgale4}`);
    console.log(`chiffreTrois : ${chiffreTrois}`);
    console.log(`deuxEtQuatrePresent : ${deuxEtQuatrePresent}`);
    return { deuxEgale4, chiffreTrois, deuxEtQuatrePresent };
};
